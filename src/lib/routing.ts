// Calculate bearing (direction) between two points
const calculateBearing = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const dLng = toRadians(lng2 - lng1);
  const y = Math.sin(dLng) * Math.cos(toRadians(lat2));
  const x = Math.cos(toRadians(lat1)) * Math.sin(toRadians(lat2)) - 
            Math.sin(toRadians(lat1)) * Math.cos(toRadians(lat2)) * Math.cos(dLng);
  return (toDegrees(Math.atan2(y, x)) + 360) % 360;
}

// Calculate distance from a point to a line
const pointToLineDistance = (A: { lat: number; lng: number }, B: { lat: number; lng: number }, P: { lat: number; lng: number }): number => {
  const toRad = (deg: number) => deg * (Math.PI / 180);
  
  const R = 6371; // Earth radius in km
  const latA = toRad(A.lat);
  const lngA = toRad(A.lng);
  const latB = toRad(B.lat);
  const lngB = toRad(B.lng);
  const latP = toRad(P.lat);
  const lngP = toRad(P.lng);
  
  // Formula haversine untuk jarak ke garis geodesik
  const d13 = Math.acos(Math.sin(latA) * Math.sin(latP) + Math.cos(latA) * Math.cos(latP) * Math.cos(lngP - lngA));
  const d12 = Math.acos(Math.sin(latA) * Math.sin(latB) + Math.cos(latA) * Math.cos(latB) * Math.cos(lngB - lngA));
  const d23 = Math.acos(Math.sin(latB) * Math.sin(latP) + Math.cos(latB) * Math.cos(latP) * Math.cos(lngP - lngB));
  
  const a = Math.sin(d13 / (2 * R)) ** 2;
  const b = Math.sin(d12 / (2 * R)) ** 2;
  const c = Math.sin(d23 / (2 * R)) ** 2;
  
  return 2 * R * Math.asin(Math.sqrt(a - b * c));
}

// Find toll gate aligned with route direction
const findDirectionalTollGate = (start: Location, end: Location): { name: string; lat: number; lng: number } | null => {
  const bearingThreshold = 30; // Maksimal deviasi 30 derajat
  const directBearing = calculateBearing(start.lat, start.lng, end.lat, end.lng);
  
  let bestGate = null;
  let bestScore = Infinity;
  
  TOLL_GATES.forEach(gate => {
    // Hitung bearing dari start ke gate
    const gateBearing = calculateBearing(start.lat, start.lng, gate.lat, gate.lng);
    
    // Hitung selisih bearing
    const bearingDiff = Math.abs(gateBearing - directBearing);
    const minDiff = Math.min(bearingDiff, 360 - bearingDiff);
    
    // Hitung jarak dari garis lurus rute
    const A = { lat: start.lat, lng: start.lng };
    const B = { lat: end.lat, lng: end.lng };
    const P = { lat: gate.lat, lng: gate.lng };
    const distanceToRoute = pointToLineDistance(A, B, P);
    
    // Skor gabungan (bearing + jarak)
    const score = minDiff * 2 + distanceToRoute;
    
    if (minDiff < bearingThreshold && score < bestScore) {
      bestScore = score;
      bestGate = gate;
    }
  });
  
  return bestGate;
}

// Find nearest toll gate to a location
const findNearestTollGate = (location: Location): { name: string; lat: number; lng: number } => {
  let nearest = TOLL_GATES[0]
  let shortestDistance = calculateDistance(location.lat, location.lng, nearest.lat, nearest.lng)
  
  TOLL_GATES.forEach(gate => {
    const distance = calculateDistance(location.lat, location.lng, gate.lat, gate.lng)
    if (distance < shortestDistance) {
      shortestDistance = distance
      nearest = gate
    }
  })
  
  return nearest
}

// Create highway-focused route with toll preference
export const calculateRoute = async (
  departure: Location,
  stops: Location[],
  destination: Location,
): Promise<RouteResponse> => {
  try {
    // Temukan gerbang tol terdekat untuk titik awal dan akhir
    const entryTollGate = findNearestTollGate(departure);
    const exitTollGate = findNearestTollGate(destination);

    // Bangun waypoints dengan gerbang tol
    const tollOptimizedPoints: Location[] = [departure];
    const usedTollGates: { name: string; lat: number; lng: number }[] = [];

    // Tambahkan gerbang masuk jika ditemukan
    if (entryTollGate) {
      tollOptimizedPoints.push({
        name: `Entry ${entryTollGate.name}`,
        lat: entryTollGate.lat,
        lng: entryTollGate.lng
      });
      usedTollGates.push(entryTollGate);
    }

    tollOptimizedPoints.push(...stops);

    // Tambahkan gerbang keluar jika ditemukan dan berbeda dengan gerbang masuk
    if (exitTollGate && entryTollGate?.name !== exitTollGate.name) {
      tollOptimizedPoints.push({
        name: `Exit ${exitTollGate.name}`,
        lat: exitTollGate.lat,
        lng: exitTollGate.lng
      });
      usedTollGates.push(exitTollGate);
    }

    tollOptimizedPoints.push(destination);
    
    // Build waypoints string for OSRM with toll-optimized points
    const waypoints = tollOptimizedPoints.map((point) => `${point.lng},${point.lat}`).join(";");
    
    console.log(" Using toll-optimized route with", tollOptimizedPoints.length, "waypoints");
    console.log(" Toll gates:", usedTollGates.map(g => g.name).join(", "));
    
    const response = await fetch(
      `${OSRM_BASE_URL}/route/v1/driving/${waypoints}?overview=full&geometries=geojson&annotations=true`
    );
    
    if (!response.ok) {
      throw new Error("Failed to calculate toll-optimized route");
    }
    
    const data = await response.json();
    
    if (!data.routes || data.routes.length === 0) {
      throw new Error("No route found");
    }
    
    const route = data.routes[0];
    const routeCoordinates = route.geometry.coordinates.map((coord: [number, number]) => ({
      lat: coord[1],
      lng: coord[0],
    }));
    
    return {
      coordinates: routeCoordinates,
      distance: route.distance / 1000, // Convert to kilometers
      duration: Math.round(route.duration / 60), // Convert to minutes
      tollGates: usedTollGates,
    };
  } catch (error) {
    console.error("Error calculating toll route:", error);
    
    // Fallback: enhanced route with highway preference
    return calculateHighwayFallbackRoute(departure, stops, destination);
  }
}

// Enhanced fallback route calculation with highway preference
const calculateHighwayFallbackRoute = (departure: Location, stops: Location[], destination: Location): RouteResponse => {
  const allPoints = [departure, ...stops, destination]
  const coordinates: { lat: number; lng: number }[] = []
  let totalDistance = 0
  const usedTollGates: { name: string; lat: number; lng: number }[] = []

  // Generate enhanced interpolated points with highway routing simulation
  for (let i = 0; i < allPoints.length - 1; i++) {
    const start = allPoints[i]
    const end = allPoints[i + 1]

    // Calculate distance between points
    const segmentDistance = calculateDistance(start.lat, start.lng, end.lat, end.lng)
    totalDistance += segmentDistance

    // For long distances, simulate highway routing
    if (segmentDistance > 50) {
      const tollEntry = findNearestTollGate(start)
      const tollExit = findNearestTollGate(end)
      
      usedTollGates.push(tollEntry, tollExit)
      
      // Route via toll gates: start -> toll entry -> toll exit -> end
      const segments = [
        { from: start, to: { name: tollEntry.name, lat: tollEntry.lat, lng: tollEntry.lng } },
        { from: { name: tollEntry.name, lat: tollEntry.lat, lng: tollEntry.lng }, to: { name: tollExit.name, lat: tollExit.lat, lng: tollExit.lng } },
        { from: { name: tollExit.name, lat: tollExit.lat, lng: tollExit.lng }, to: end }
      ]
      
      segments.forEach(segment => {
        // Generate more points for highway segments (smoother animation)
        const points = segment.from.name?.includes("Tol") || segment.to.name?.includes("Tol") ? 30 : 15
        for (let j = 0; j <= points; j++) {
          const ratio = j / points
          coordinates.push({
            lat: segment.from.lat + (segment.to.lat - segment.from.lat) * ratio,
            lng: segment.from.lng + (segment.to.lng - segment.from.lng) * ratio,
          })
        }
      })
    } else {
      // Direct route for short distances
      for (let j = 0; j <= 20; j++) {
        const ratio = j / 20
        coordinates.push({
          lat: start.lat + (end.lat - start.lat) * ratio,
          lng: start.lng + (end.lng - start.lng) * ratio,
        })
      }
    }
  }

  // Highway speed estimate: 70-80 km/h average
  const highwaySpeed = 75
  const estimatedDuration = Math.round((totalDistance / highwaySpeed) * 60)

  console.log(" Fallback highway route:", {
    distance: totalDistance.toFixed(1) + "km",
    duration: estimatedDuration + " minutes",
    tollGates: usedTollGates.length
  })

  return {
    coordinates,
    distance: totalDistance,
    duration: estimatedDuration,
    tollGates: usedTollGates,
  }
}

// Calculate distance between two coordinates using Haversine formula
export const calculateDistance = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const R = 6371 // Earth's radius in kilometers
  const dLat = toRadians(lat2 - lat1)
  const dLng = toRadians(lng2 - lng1)

  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(toRadians(lat1)) * Math.cos(toRadians(lat2)) * Math.sin(dLng / 2) * Math.sin(dLng / 2)

  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
  return R * c
}

const toRadians = (degrees: number): number => {
  return degrees * (Math.PI / 180)
}

const toDegrees = (radians: number): number => {
  return radians * (180 / Math.PI)
}